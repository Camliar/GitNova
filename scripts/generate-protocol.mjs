import { readFile, writeFile } from "node:fs/promises";
import process from "node:process";

const schemaUrl = new URL("../sdk/protocol/gitnova-protocol.schema.json", import.meta.url);
const outputUrl = new URL("../packages/protocol/src/generated.ts", import.meta.url);
const schema = JSON.parse(await readFile(schemaUrl, "utf8"));
const requiredDefinitions = ["RequestId", "ImplementationInfo", "ClientCapabilities", "ServerCapabilities", "InitializeParams", "InitializeResult", "CancelParams", "ErrorData", "RepositoryPathParams", "RepositoryKind", "RepositoryDescriptor", "StatusEntryKind", "FileStatus", "StatusEntry", "BranchStatus", "WorkingTreeStatus", "DiffScope", "DiffParams", "DiffLineKind", "DiffLine", "DiffHunk", "FileDiff", "HistoryParams", "CommitIdentity", "CommitSummary", "HistoryPage", "CommitDiffParams", "CommitDiff"];
for (const name of requiredDefinitions) {
  if (!schema.$defs?.[name]) throw new Error(`Protocol schema is missing $defs.${name}`);
}
const protocolVersion = schema.properties?.protocolVersion?.const;
if (typeof protocolVersion !== "string") throw new Error("Protocol schema must declare properties.protocolVersion.const");

const generated = `// Generated by scripts/generate-protocol.mjs. Do not edit manually.

export const PROTOCOL_VERSION = ${JSON.stringify(protocolVersion)} as const;

export type RequestId = number | string;

export interface ImplementationInfo {
  name: string;
  version: string;
}

export interface ClientCapabilities {
  cancellation?: boolean;
}

export interface ServerCapabilities {
  cancellation: boolean;
  repositoryDiscovery: boolean;
  workingTreeStatus: boolean;
  structuredFileDiff: boolean;
  paginatedCommitHistory: boolean;
  structuredCommitDiff: boolean;
}

export interface InitializeParams {
  clientInfo: ImplementationInfo;
  protocolVersion: string;
  capabilities?: ClientCapabilities;
}

export interface InitializeResult {
  coreInfo: ImplementationInfo;
  protocolVersion: string;
  capabilities: ServerCapabilities;
}

export interface CancelParams {
  id: RequestId;
}

export interface ErrorData {
  stableCode: string;
  retryable: boolean;
  details?: Record<string, unknown>;
}

export interface RepositoryPathParams {
  path: string;
}

export type RepositoryKind = "worktree" | "linkedWorktree" | "bare";

export interface RepositoryDescriptor {
  worktreeRoot: string | null;
  gitDirectory: string;
  commonGitDirectory: string;
  kind: RepositoryKind;
  gitVersion: string;
}

export type StatusEntryKind = "ordinary" | "renameOrCopy" | "unmerged" | "untracked";

export type FileStatus = "unmodified" | "modified" | "added" | "deleted" | "renamed" | "copied" | "unmerged" | "untracked" | "typeChanged" | "unknown";

export interface StatusEntry {
  path: string;
  originalPath: string | null;
  kind: StatusEntryKind;
  indexStatus: FileStatus;
  worktreeStatus: FileStatus;
}

export interface BranchStatus {
  head: string | null;
  oid: string | null;
  upstream: string | null;
  ahead: number;
  behind: number;
}

export interface WorkingTreeStatus {
  branch: BranchStatus;
  entries: StatusEntry[];
}

export type DiffScope = "workingTree" | "staged";

export interface DiffParams {
  path: string;
  scope: DiffScope;
  contextLines?: number;
}

export type DiffLineKind = "context" | "addition" | "deletion";

export interface DiffLine {
  kind: DiffLineKind;
  content: string;
  oldLine: number | null;
  newLine: number | null;
}

export interface DiffHunk {
  oldStart: number;
  oldLines: number;
  newStart: number;
  newLines: number;
  header: string;
  lines: DiffLine[];
}

export interface FileDiff {
  oldPath: string;
  newPath: string;
  isBinary: boolean;
  hunks: DiffHunk[];
}

export interface HistoryParams {
  limit?: number;
  cursor?: string;
}

export interface CommitIdentity {
  name: string;
  email: string;
  timestamp: string;
}

export interface CommitSummary {
  oid: string;
  parents: string[];
  author: CommitIdentity;
  committer: CommitIdentity;
  summary: string;
  message: string;
}

export interface HistoryPage {
  commits: CommitSummary[];
  nextCursor: string | null;
}

export interface CommitDiffParams {
  oid: string;
  parentOid?: string;
  contextLines?: number;
}

export interface CommitDiff {
  commit: CommitSummary;
  parentOid: string | null;
  files: FileDiff[];
}
`;

if (process.argv.includes("--check")) {
  const existing = await readFile(outputUrl, "utf8").catch(() => "");
  if (existing !== generated) throw new Error("Generated protocol types are out of date; run npm run generate:protocol");
} else {
  await writeFile(outputUrl, generated);
}
